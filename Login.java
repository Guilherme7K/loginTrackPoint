    private void btnEntrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEntrarActionPerformed
       // 1 Pegar os dados digitados nnos campos e guardar em variaveis
        String L, S, U;
        L = txtUsername.getText();
        S = txtSenha.getText();
        U = cmbUsuario.getSelectedItem().toString();
        
        try {
            // 2 Conectar ao Banco de dados da Empresa
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection conectar = DriverManager.getConnection("");
            //3 enviar os daos para a tabela do BD
            PreparedStatement st = conectar.prepareStatement("IINSERT INTO usuarios VALUES(?,?,?)");
            st.setString(1, L);
            st.setString(2, S);
            st.setString(3, U);
            st.executeUpdate(); // Executa o comando INSERT
            // 4 exibir uma mensagem para o usuario 
            JOptionPane.showMessageDialog(null, "Usu√°rio cadastrado");
            //5 limpar os campos
            txtUsername.setText("");
            txtSenha.setText("");
            txtUsername.requestFocus();//posicionar o campo usuario
            
        } catch (ClassNotFoundException ex) {
            JOptionPane.showMessageDialog(null, "Entre em contato com suporte e informe o erro:" + ex.getMessage());
        } catch (SQLException ex) {
           JOptionPane.showMessageDialog(null, "Entre em contato com suporte e informe o erro:" + ex.getMessage());
        }
    }//GEN-LAST:event_btnEntrarActionPerformed

